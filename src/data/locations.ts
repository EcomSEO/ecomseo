/** Check if a location is live (no goLiveDate or goLiveDate <= today) */
export function isLive(loc: Location): boolean {
  if (!loc.goLiveDate) return true;
  const now = new Date();
  const goLive = new Date(loc.goLiveDate);
  return now >= goLive;
}

/** All locations across all locales (only live ones) */
export function getAllLocations(): Location[] {
  return [...nlLocations, ...frLocations, ...deLocations, ...atLocations, ...esLocations, ...itLocations, ...enLocations, ...beLocations].filter(isLive);
}

/** All locations including not-yet-live (for build-time static params) */
export function getAllLocationsIncludingScheduled(): Location[] {
  return [...nlLocations, ...frLocations, ...deLocations, ...atLocations, ...esLocations, ...itLocations, ...enLocations, ...beLocations];
}

/** Get locations for a specific locale */
export function getLocationsByLocale(locale: Locale): Location[] {
  return getAllLocations().filter((loc) => loc.locale === locale);
}

/** Get a single location by internal slug (only if live) */
export function getLocation(slug: string): Location | null {
  return getAllLocations().find((loc) => loc.slug === slug) ?? null;
}

/** Get a single location by slug (including scheduled/not-yet-live) */
export function getLocationIncludingScheduled(slug: string): Location | null {
  return getAllLocationsIncludingScheduled().find((loc) => loc.slug === slug) ?? null;
}

/** Get all internal slugs for generateStaticParams (includes scheduled) */
export function getAllLocationSlugs(): string[] {
  return getAllLocationsIncludingScheduled().map((loc) => loc.slug);
}

/** Get footer-worthy locations (Tier 1 only) for a locale */
export function getFooterLocations(locale: Locale): Location[] {
  return getLocationsByLocale(locale)
    .filter((loc) => loc.tier === 1)
    .sort((a, b) => b.searchVolume - a.searchVolume);
}
